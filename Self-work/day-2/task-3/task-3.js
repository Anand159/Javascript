// Push : add to end.
let cars=["maruti","audi","bmw", "xuv"];
cars.push("toyota");
console.log(cars);

// pop: delete from end & returns it.
cars.pop();
console.log(cars);


// unshift : add to start. 
cars.unshift("thar");
console.log(cars);

// shift: delete from start & returns it.
cars.shift();
console.log(cars);




